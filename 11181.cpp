double DP( int pos , double P , int taken )
{
    if( pos == n )
    {
        if( taken == r ) return P ;
        else return 0.0 ;
    }
    double ans1 = 0 , ans2 = 0 ;
    if( taken < r ) // its possible to buy
    {
       ans1 = DP( pos + 1 , P * p[pos] , taken + 1 );
       tot[ pos ] += ans1 ;
    }
    ans2 = DP( pos + 1 , P * ( 1 - p[pos] ) , taken );
    return ans1 + ans2 ;

}
